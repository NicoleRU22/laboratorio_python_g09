"""tipos de datos trascedentales"""
"""tipo de dato: lista  []"""

var1 = [20,"Luis","Calle Las Almendras", 1993]

print("El valor de mi variable var1 es: {}".format(var1))

"""tipo de dato: tupla()"""
var2 = (48,89.6,178,2024,"Hola Python")
print("El valor de mi variable var2 es: {}".format(var2))

"""tipo de dato: set()"""
var3 = set(["Bienvenidos", 50, 150, 40.79, False, 100, 50, 50, 150])
print("El valor de mi variable var3 es: {}".format(var3))


"""tipo de dato diccionario: {}"""
var4={ "nombre": "Mario", "edad": 28, "dni": 75963215}
print("El valor de mi variable var4 es: {}".format(var4))

